package main.games;

/**
 * Class Game is an abstract class that sits at the top of the Game hierarchy.
 * It declares a single abstract method - void play().
 * All subclasses must implement this method is some fashion.
 *
 * @author: Nathan Lauer
 * @email: lauern@bu.edu
 * Creation Date: 11/4/20
 * <p>
 * Please feel free to ask me any questions. I hope you're having a nice day!
 */
public abstract class Game {
    /**
     * Plays the Game.
     */
    public abstract void play();
}
